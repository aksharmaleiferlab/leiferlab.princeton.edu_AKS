<li>Carrying out independent investigations that focus on transgenics and molecular genetics.</li>
<li>Training incoming lab members in safe nematode handling, maintenance and transgenics.</li>
<li>Mentoring undergraduate students and serving as a resource for graduate students.</li>
<li>Developing and maintaining protocols for working with C. elegans </li>
