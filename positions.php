<h2><a name="professional"></a>Research Specialist</h2>
<p>A Research Specialist is sought to perform nematode transgenics in support of research efforts in the Leifer Lab. The Leifer Lab studies patterns of neural activity that drive behavior in the nematode C. elegans using novel imaging approaches that combine techniques from physics, neuroscience and molecular biology.</p> 

Specific duties include:
<ul>
    <li>Maintaining C. elegans stocks for experimentation.</li>
    <li>Performing genetic crosses.</li>
    <li>Screening transgenic animals.</li>
    <li>Performing molecular cloning and plasmid design.</li>
    <li>Microinjecting C. elegans for creating new transgenic nematode strains.</li>
    <li>Preparing media, pouring plates, and washing glassware.</li>
    <li>Preparing, organizing and purchasing equipment, supplies and reagents.</li>
    <li>Other support duties to facilitate a safe, productive and efficient lab environment.</li>
    <li>Conducting occasional laboratory experiments in support of others and carefully managing and recording experimental conditions.</li>
    <li>    Recording and analyzing fluorescent microscopy images and videos.</li>
    <li>Familiarize her or himself with the current literature and projects of the lab.</li>
    <li>Perform other related duties as assigned.</li>
</ul>

ESSENTIAL Qualificiations:
<ul>
    <li>BA or BS in biology, neuroscience, bioengineering or related field.</li>
    <li>Prior experience with genetics, molecular cloning and PCR.</li>
    <li>Prior experience working with an invertebrate genetic model system.</li>
    <li>Ability to conduct laboratory experiments independently and carefully manage and record experimental conditions.</li>
    <li>Ability to maintain a meticulous daily laboratory notebook.</li>
    <li>Ability to work well with others.</li>
    <li>Ability to read and extract information from primary scientific literature.</li>
    <li>Excellent computer skills with proficiency in Microsoft Word, Excel and PowerPoint.</li>
    <li>Applicant must have excellent organizational skills and flexibility to handle multiple tasks simultaneously.</li>
    <li>The applicant should have a strong attention to detail and an independent problem-solving mentality.</li>
</ul>

PREFERRED Qualifications:
<ul>
    <li>Prior experience working with C. elegans.</li>
    <li>Prior experience with microinjection.</li>
    <li>Prior experience with Gibson cloning techniques.</li>
</ul>

